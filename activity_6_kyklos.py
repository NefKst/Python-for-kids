# Αρχικά ορίζουμε μια μεταβλητή που αποθηκεύει το μήκος της ακτίνας (δεκαδικός αριθμός) που θα εισάγει ο χρήστης.
# Ορίζουμε ποιο είναι το π
# Υπολογίζουμε τη διάμετρο και την εμφανίζουμε.
# Υπολογίζουμε το μήκος του κύκλου και το εμφανίζουμε.

x = float(input("Εισάγετε την ακτίνα του κύκλου: "))
pi = float(3.14159)
print ("Η διάμετρος του κύκλου είναι: " + str(x * 2))
print ("Το μήκος του κύκλου είναι: " + str (2 * x * pi))
