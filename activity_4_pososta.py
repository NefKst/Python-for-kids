# γραμμή 2: αλγόριθμος για τον υπολογισμό της έκπτωσης - για έκπτωση 30% η τιμή (x) πολ/ζεται με 100-30 (70) και διαιρείται με το 100 - το αποτέλεσμα ορίζει τη μεταβλητή y
